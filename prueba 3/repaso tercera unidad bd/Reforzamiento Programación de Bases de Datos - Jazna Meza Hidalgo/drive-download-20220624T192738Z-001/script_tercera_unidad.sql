CREATE TABLE asig_escolaridad
(id_escolaridad  NUMBER(2) NOT NULL,
 sigla_escolaridad  VARCHAR2(6) NOT NULL,
 desc_escolaridad VARCHAR2(50) NOT NULL,
 porc_asig_escolaridad NUMBER(2),
 CONSTRAINT pk_escolaridad PRIMARY KEY (id_escolaridad));
 
 
CREATE TABLE AFP
(cod_afp NUMBER(2) CONSTRAINT PK_AFP PRIMARY KEY,
 nombre_afp  VARCHAR2(30) NOT NULL,
 porc_descto_afp NUMBER(2) NOT NULL);

CREATE TABLE SALUD
(cod_salud NUMBER(2) CONSTRAINT PK_SALUD PRIMARY KEY,
 nombre_salud  VARCHAR2(30) NOT NULL,
 porc_descto_salud NUMBER(2) NOT NULL);


CREATE TABLE empleado
(numrut_emp NUMBER(10) NOT NULL,
 dvrut_emp  VARCHAR2(1) NOT NULL,
 appaterno_emp VARCHAR2(15) NOT NULL,
 apmaterno_emp VARCHAR2(15) NOT NULL,
 nombre_emp VARCHAR2(25) NOT NULL,
 direccion_emp VARCHAR2(60) NOT NULL,
 fonofijo_emp VARCHAR2(15) NOT NULL,
 celular_emp VARCHAR2(15),
 fecnac_emp DATE,
 fecing_emp DATE NOT NULL,
 sueldo_base_emp NUMBER(7) NOT NULL,
 id_comuna NUMBER(3),
 id_categoria_emp NUMBER(1),
 id_escolaridad  NUMBER(2) NOT NULL,
 cod_afp  NUMBER(2) NOT NULL,
 cod_salud NUMBER(2) NOT NULL,
 CONSTRAINT PK_EMPLEADO PRIMARY KEY (numrut_emp),
 CONSTRAINT FK_EMPLEADO_COMUNA FOREIGN KEY (id_comuna) REFERENCES comuna (id_comuna),
CONSTRAINT FK_EMPLEADO_AFP FOREIGN KEY (cod_afp) REFERENCES afp (cod_afp),
CONSTRAINT FK_EMPLEADO_SALUD FOREIGN KEY (cod_salud) REFERENCES salud (cod_salud),
 CONSTRAINT FK_EMPLEADO_ESCOLARIDAD FOREIGN KEY(id_escolaridad) REFERENCES asig_escolaridad(id_escolaridad),
 CONSTRAINT FK_EMPLEADO_CATEGORIA_EMPLEADO FOREIGN KEY (id_categoria_emp) REFERENCES categoria_empleado (id_categoria_emp));

CREATE TABLE CARGA_FAMILIAR
(numrut_carga  NUMBER(10) NOT NULL CONSTRAINT PK_CARGA_FAMILIAR PRIMARY KEY,
 dvrut_carga  VARCHAR2(1) NOT NULL,
 appaterno_carga VARCHAR2(15) NOT NULL,
 apmaterno_carga VARCHAR2(15) NOT NULL,
 nombre_carga VARCHAR2(25) NOT NULL,
 numrut_emp NUMBER(10) NOT NULL,
 CONSTRAINT FK_CARGA_FAMILIAR_VENDEDOR FOREIGN KEY(numrut_emp) REFERENCES empleado(numrut_emp)); 

CREATE TABLE BOLETA 
(nro_boleta NUMBER(10) CONSTRAINT PK_BOLETA PRIMARY KEY , 
 fecha_boleta DATE  NOT NULL , 
 monto_boleta NUMBER(15)  NOT NULL , 
 id_cliente NUMBER(10) NOT NULL,
 numrut_emp NUMBER(10) NOT NULL,
 CONSTRAINT FK_FACTURA_VENDEDOR FOREIGN KEY(numrut_emp) REFERENCES empleado(numrut_emp)); 

CREATE TABLE PORC_BONIF_ANNOS_CONTRATO
(sec_annos_contratado NUMBER(2) CONSTRAINT PK_ANNOS_TRABAJADOS PRIMARY KEY,
 annos_inferior     NUMBER(2) NOT NULL,
 annos_superior     NUMBER(2) NOT NULL,
 porc_bonif         NUMBER(3,2) NOT NULL);

CREATE TABLE COMISION_VENTA
(nro_boleta NUMBER(10) NOT NULL CONSTRAINT PK_COMISION_VENTA PRIMARY KEY ,
 valor_comision  NUMBER(10) NOT NULL,
 CONSTRAINT FK_COMISION_VENTA_BOLETA FOREIGN KEY(nro_boleta) REFERENCES BOLETA(nro_boleta));

CREATE TABLE ESTADO_CIVIL_EMPLEADO
(numrut_emp NUMBER(10) NOT NULL,
 id_estcivil NUMBER(1) NOT NULL,
 fecini_estcivil DATE NOT NULL,
 fecter_estcivil DATE,
 CONSTRAINT PK_ESTADO_CIVIL_EMPLEADO PRIMARY KEY(numrut_emp, fecini_estcivil),
 CONSTRAINT FK_EST_CIVIL_EMPLEADO FOREIGN KEY(numrut_emp) REFERENCES empleado(numrut_emp)); 

CREATE TABLE ERROR_PROCESO_REMUN
(correl_error      NUMBER(5) NOT NULL CONSTRAINT PK_ERROR_PROC_REMUN PRIMARY KEY,
 rutina_error      VARCHAR2(150) NOT NULL,
 descrip_error     VARCHAR2(200) NOT NULL); 


CREATE TABLE HABER_CALC_MES
(numrut_emp NUMBER(10) NOT NULL,
 mes_proceso NUMBER(2) NOT NULL,
